	diffuseColor *= 0.0;
	ReflectedLight reflectedLight = ReflectedLight( vec3( 0.0 ), vec3( 0.0 ), vec3( 0.0 ), vec3( 0.0 ) );
	vec3 totalEmissiveRadiance = emissive;
	
	roughness = ( 1.0 - smoothstep( 0.0, 1.0,  texture2D( roughnessTex, texUV * 3.0  ).x ) ) * 0.5;

	vec3 ntex = texture2D( normalTex, texUV * 10.0 ).xyz * 2.0 - 1.0;
